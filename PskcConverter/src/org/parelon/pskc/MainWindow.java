    private void browseFileInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_browseFileInMouseClicked
        try {
            this.jFileChooser1.setSelectedFile(null);
            this.jFileChooser1.resetChoosableFileFilters();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("PSKC file", "xml");
            this.jFileChooser1.setFileFilter(filter);
            this.jFileChooser1.setVisible(true);
            if (jFileChooser1.showOpenDialog(jLabel1) == JFileChooser.APPROVE_OPTION) {

                this.fileInTextField.setText(jFileChooser1.getSelectedFile().getAbsolutePath());
                if (System.getProperty("os.name").startsWith("Windows")) {
                    this.fileInTextField.setText(jFileChooser1.getSelectedFile().getAbsolutePath().substring(jFileChooser1.getSelectedFile().getAbsolutePath().lastIndexOf("\\") + 1));
                    this.folderOutTextField.setText(jFileChooser1.getSelectedFile().getAbsolutePath().substring(0, jFileChooser1.getSelectedFile().getAbsolutePath().lastIndexOf("\\")));
                } else {
                    this.fileInTextField.setText(jFileChooser1.getSelectedFile().getAbsolutePath().substring(jFileChooser1.getSelectedFile().getAbsolutePath().lastIndexOf("/") + 1));
                    this.folderOutTextField.setText(jFileChooser1.getSelectedFile().getAbsolutePath().substring(0, jFileChooser1.getSelectedFile().getAbsolutePath().lastIndexOf("/")));
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_browseFileInMouseClicked

