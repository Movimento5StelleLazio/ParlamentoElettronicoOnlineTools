    private void convertiButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_convertiButtonMouseClicked
        aesKeyTextField.setText("Inizializzazione");
        new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    if (privateKeyFrammento1TextField.getText().equals("") || privateKeyFrammento1TextField.getText().equals("")) {
                        throw new Exception("Seleziona tutte le chiavi prima.");
                    }

                    chiudiButton.setEnabled(false);
                    convertiButton.setEnabled(false);
                    browseFileIn.setEnabled(false);
                    //browseFolderOut.setEnabled(false);
                    browsePrivateKeyFrammento1.setEnabled(false);
                    browsePrivateKeyFrammento2.setEnabled(false);

                    XmlManager manager = new XmlManager(manufactuerTextField.getText(), issuerTextField.getText());
                    aesKeyTextField.setText("Generazione chiavi");
                    aesKeyTextField.setText(manager.initializeCrypto(fragment1, fragment2));
                    if (System.getProperty("os.name").startsWith("Windows")) {
                        manager.convertXml(folderOutTextField.getText() + "\\" + fileInTextField.getText(), folderOutTextField.getText() + "\\new_" + fileInTextField.getText());
                    } else {
                        manager.convertXml(folderOutTextField.getText() + "/" + fileInTextField.getText(), folderOutTextField.getText() + "/new_" + fileInTextField.getText());
                    }

                    JOptionPane.showMessageDialog(null, "XML convertito correttamente. Importare l'XML sul server di autenticazione inserendo la chiave AES indicata. NB: NON PERDERLO!");

                    fragment1 = null;
                    fragment2 = null;
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Errore: " + ex.getMessage());
                    ex.printStackTrace();
                }
                chiudiButton.setEnabled(true);
                convertiButton.setEnabled(true);
                browseFileIn.setEnabled(true);
                //browseFolderOut.setEnabled(true);
                browsePrivateKeyFrammento1.setEnabled(true);
                browsePrivateKeyFrammento2.setEnabled(true);
                privateKeyFrammento1TextField.setText("");
                privateKeyFrammento2TextField.setText("");
            }
        }).start();
    }//GEN-LAST:event_convertiButtonMouseClicked

    private void browsePrivateKeyFrammento1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_browsePrivateKeyFrammento1MouseClicked
        try {
            //this.jFileChooser1 = new JFileChooser();
            this.jFileChooser1.setSelectedFile(null);
            this.jFileChooser1.resetChoosableFileFilters();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("KEY fragment", "key");
            this.jFileChooser1.setFileFilter(filter);
            this.jFileChooser1.setVisible(true);
            if (jFileChooser1.showOpenDialog(jLabel1) == JFileChooser.APPROVE_OPTION) {
                this.privateKeyFrammento1TextField.setText(jFileChooser1.getSelectedFile().getCanonicalPath());
                File f = new File(this.privateKeyFrammento1TextField.getText());
                FileInputStream fis = new FileInputStream(f);
                DataInputStream dis = new DataInputStream(fis);
                byte[] temp = new byte[(int) f.length()];
                dis.readFully(temp);
                dis.close();
                this.fragment1 = Base64.decode(temp);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_browsePrivateKeyFrammento1MouseClicked

